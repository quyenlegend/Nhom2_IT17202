    private void loadDataToTableSize() {
        List<model.QLSize> ds = sizeService.getProducts();
        DefaultTableModel dtm = (DefaultTableModel) this.tblSize.getModel();
        dtm.setRowCount(0);
        for (QLSize product : ds) {
            Object[] rowData = {
                product.getMaSize(),
                product.getSize()
            };
            
            dtm.addRow(rowData);
        }
        
    }
    
