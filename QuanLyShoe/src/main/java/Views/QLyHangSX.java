    private void loadDataToTableHSX() {
        List<HangModel> ds = hangService.getProducts();
        DefaultTableModel dtm = (DefaultTableModel) this.tblHSX.getModel();
        dtm.setRowCount(0);
        for (HangModel product : ds) {
            Object[] rowData = {
                product.getMaHangSX(),
                product.getTenHang()
            };
            
            dtm.addRow(rowData);
        }
        
    }
    
    private HangModel getSanPhamFromInput(){
        HangModel qlHangSX = new HangModel();
        String proHSX = txtHSX.getText();
        qlHangSX.setTenHang(proHSX);
        return qlHangSX;
    }
